package io.karatelabs.driver.cdp;

import java.util.Map;

/**
 * Listener interface for CDP events.
 * Allows external code to receive all CDP events for purposes like recording.
 */
public interface CdpEventListener {

    /**
     * Called when a CDP event is received.
     *
     * @param method CDP event method name (e.g., "Network.requestWillBeSent")
     * @param params Event parameters as a map
     */
    void onEvent(String method, Map<String, Object> params);
}
